 private void jTTabelaLinhaSelecionada(JTable tabela) {
           if (jTable1.getSelectedRow()!= -1) {     
          String cod = (jTable1.getValueAt(jTable1.getSelectedRow(),0)).toString();
                   this.idMarca = Integer.parseInt(cod);
                   this.nomeMarca = (jTable1.getValueAt(jTable1.getSelectedRow(),1)).toString();
                  // jTMarca.setText(nomeMarca);
                   String idFOrn   = (jTable1.getValueAt(jTable1.getSelectedRow(),2)).toString();
                        this.id_Forn  = Integer.parseInt(idFOrn);
                   this.Modelo = (jTable1.getValueAt(jTable1.getSelectedRow(),3)).toString();
                   
          
                   jogaValor();
           }
     }
