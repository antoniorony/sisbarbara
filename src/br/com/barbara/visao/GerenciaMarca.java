     private void mostraPesquisaFiltro(String text) {
        tabela = (DefaultTableModel)jTable1.getModel();
        tabela.setNumRows(0);
        Session sesion = HibernateUtil.getSessionFactory().openSession();
        Transaction tx = null;
        Criteria crit = sesion.createCriteria(Fornecedores.class);
        crit.add(Restrictions.like("nomeForn",text+"%"));
        java.util.List results = crit.list();
        for (Iterator iterator = results.iterator(); iterator.hasNext();){
        Fornecedores Sal = (Fornecedores) iterator.next(); 
        
 
        JMenuItem item = new JMenuItem(Sal.getNomeForn()+","+Sal.getIdForn());
          item.addActionListener(new ActionListener() {
             
            @Override
            public void actionPerformed(ActionEvent arg0)
            {
                String mods = item.getText();
                 String campos[] = mods.split(",");
                jTFornecedor.setText(campos[0]);
              
                id_Forn = Integer.parseInt(campos[1]);
             }
          });
        
        
        jPopupF.add(item);
        
        }    }

